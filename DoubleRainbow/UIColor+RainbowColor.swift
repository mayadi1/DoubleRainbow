import UIKit

extension UIColor {
    
    
    
    static func indigoColor() -> UIColor{
        return UIColor(red: 0.2, green: 0.0, blue: 0.41, alpha: 1.0)
    }
    
    static func violetColor() -> UIColor{
    
        return UIColor(red: 0.451, green: 0.4, blue: 0.741, alpha: 1.0)
    }
}